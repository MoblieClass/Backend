package com.wj2025.mobileclass.model.course;

public class CourseModel {
    private String id;
    private String course_name;
    private String teacher_name;
    private String course_type;
    private String classroom;
    private String start_time;
    private String end_time;
    private String start_week;
    private String end_week;
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getCourse_name() {
        return course_name;
    }
    public void setCourse_name(String course_name) {
        this.course_name = course_name;
    }
    public String getTeacher_name() {
        return teacher_name;
    }
    public void setTeacher_name(String teacher_name) {
        this.teacher_name = teacher_name;
    }
    public String getCourse_type() {
        return course_type;
    }
    public void setCourse_type(String course_type) {
        this.course_type = course_type;
    }
    public String getClassroom() {
        return classroom;
    }
    public void setClassroom(String classroom) {
        this.classroom = classroom;
    }
    public String getStart_time() {
        return start_time;
    }
    public void setStart_time(String start_time) {
        this.start_time = start_time;
    }
    public String getEnd_time() {
        return end_time;
    }
    public void setEnd_time(String end_time) {
        this.end_time = end_time;
    }
    public String getStart_week() {
        return start_week;
    }
    public void setStart_week(String start_week) {
        this.start_week = start_week;
    }
    public String getEnd_week() {
        return end_week;
    }
    public void setEnd_week(String end_week) {
        this.end_week = end_week;
    }
}
